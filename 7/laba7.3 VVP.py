print('Известно, что X кг конфет стоит A рублей. Определить, сколько стоит 1 кг и Y кг этих же конфет.')
print('Введите X, A и Y:')
x, a, y = int(input()), int(input()), float(input())
print(x,'кг конфет стоят', a, 'рублей.')
ed = (a / x) * (1 + y)
print(1 + y, 'кг этих же конфет стоят ', ed, 'рублей.')